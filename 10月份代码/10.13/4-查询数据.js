// 14. 在数组中 查询数据第二次55出现的位置,打印第二次出现的索引（索引指从0开始）[11, 32，55, 47，55, 79，23]



var arr = [11, 32, 55, 47, 55, 79, 23];


var sum = 0; // 累加
var x = 0; // 储存下标
// 遍历数组，判断55出现的次数，如果次数为2，那么打印第二个55的下标
for (var i = 0; i < arr.length; i++) {
    if(arr[i] == 55){
        sum++;
    }
    if(sum == 2){
        x = i;
        break;
    }
}
console.log('第二个55的索引是:' + x);